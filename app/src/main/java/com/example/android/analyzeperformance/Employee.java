package com.example.android.analyzeperformance;

public class Employee {

    private int image;

    private String name;

    private String natl;

    private int age;

    private String occ;

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNatl() {
        return natl;
    }

    public void setNatl(String natl) {
        this.natl = natl;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getOcc() {
        return occ;
    }

    public void setOcc(String occ) {
        this.occ = occ;
    }
}
